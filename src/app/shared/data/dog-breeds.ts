// Search function for filtering breeds
export function searchBreeds(query: string): string[] {
  const lowerQuery = query.toLowerCase().trim();

  if (!lowerQuery) {
    return DOG_BREEDS.slice(0, 10);
  }

  // Find breeds that start with the query first
  const startsWithMatches = DOG_BREEDS.filter((breed) =>
    breed.toLowerCase().startsWith(lowerQuery)
  );

  // Then find breeds that contain the query
  const containsMatches = DOG_BREEDS.filter((breed) => {
    const lowerBreed = breed.toLowerCase();
    return (
      lowerBreed.includes(lowerQuery) && !lowerBreed.startsWith(lowerQuery)
    );
  });

  // Combine and limit results
  return [...startsWithMatches, ...containsMatches].slice(0, 20);
}
